package org.javaguru.student_anastassija.lesson_4;

public class RangeOfNumbers2OR {

    public static void main(String[] args) {

        int number = 2;

        if ((number >= 10) || (number <= 20)) {
            System.out.println("Number is inside range (10-20)!");
        } else {
            System.out.println("Number is NOT inside range (10-20)!");
        }

    }
}
