package org.javaguru.student_anastassija.lesson_4;

public class RangeOfNumbers {

    public static void main(String[] args) {

        int number = 2;

        if ((number >= 25) && (number <= 89)) {
            System.out.println("Number is inside range (25-89)!");
        } else {
            System.out.println("Number is NOT inside range (25-89)!");
        }

    }
}
