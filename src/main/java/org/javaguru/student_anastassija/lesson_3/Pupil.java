package org.javaguru.student_anastassija.lesson_3;

class Pupil {

    private String name;
    private int grade;


    public Pupil(String name, int grade){
        this.name = name;
        this.grade = grade;
    }

    public String getName(){
        return this.name;
    }

    public int getGrade(){
        return this.grade;

    }

}
