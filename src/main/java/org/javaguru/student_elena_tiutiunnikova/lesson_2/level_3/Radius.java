public class Radius {
    public static void main(String[] args) {


        double Radius = 7.5;
        double pi = Math.PI;

        double Perimetr = Radius * 2 * pi;
        double Area = pi * Radius * Radius;

        System.out.println("Perimetr is = " + Perimetr);
        System.out.println("Area is = " + Area);

    }

}
