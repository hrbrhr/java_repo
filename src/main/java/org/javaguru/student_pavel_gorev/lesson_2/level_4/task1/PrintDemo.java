package org.javaguru.student_pavel_gorev.lesson_2.level_4.task1;

class PrintDemo {
    public static void main(String[] args) {
        // Используем System.out.print() — текст будет выведен в одну строку
        System.out.print("Это ");
        System.out.print("пример ");
        System.out.print("вывода ");
        System.out.print("в ");
        System.out.print("одной ");
        System.out.print("строке.");

        // Перевод строки
        System.out.println();

        // Используем System.out.println() — каждая часть будет на новой строке
        System.out.println("Это");
        System.out.println("пример");
        System.out.println("вывода");
        System.out.println("на");
        System.out.println("разных");
        System.out.println("строках.");
    }
}
