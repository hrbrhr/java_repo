package org.javaguru.student_artjoms_babicevs.lesson_3.level_7.task_2;

public class BankAccount {

    private String owner;
    private int money;

    public BankAccount(String owner, int moneyAmount) {
        this.owner = owner;
        this.money = moneyAmount;
    }

   public String getOwner() {
        return this.owner;
    }

    public int getMoney() {
        return this.money;
    }

}





